#! /usr/bin/bash

# declare a variable
# NOTE: no spaces are allowed before and after the =

# number variable
num=10

# decimal variable 
salary=15.50

# string variable using double quotes
first_name="amit"

# string variable using single quotes
last_name='kulkarni'

# string variale using no quotes
company=sunbeam

# print the value of a variable on console
# NOTE: to get the value of a variable use $
echo $num
echo $salary
echo $first_name
echo $last_name
echo $company

# print values with predefined string values
# print(f"num = {num}")
echo "num = $num"
echo "salary = $salary"
echo "first name = $first_name"
echo "last name = $last_name"
echo "company = $company"







